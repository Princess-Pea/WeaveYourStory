import { useRouter } from 'vue-router'
import { onMounted, nextTick, onUnmounted } from 'vue'

const router = useRouter()

const navigateTo = (path) => {
  // è§¦å‘ç²’å­çˆ†ç‚¸æ•ˆæœ
  const btn = document.querySelector('.create-btn')
  if (btn) {
    btn.classList.add('particle-burst')
  }
  
  // ç²’å­æ•ˆæœåè·³è½¬
  setTimeout(() => {
    router.push(path)
  }, 600)
}

const onCardClick = (event) => {
  // è§¦å‘å¡ç‰‡ç²’å­çˆ†ç‚¸æ•ˆæœ
  const card = event.currentTarget
  if (card && !card.classList.contains('card-burst')) {
    card.classList.add('card-burst')
    // 1ç§’åç§»é™¤æ•ˆæœç±»ï¼Œå…è®¸å†æ¬¡è§¦å‘
    setTimeout(() => {
      card.classList.remove('card-burst')
    }, 1000)
  }
}

let animationObserver = null;

// æ¯æ¬¡è¿›å…¥é¡µé¢æ—¶é‡ç½®åŠ¨ç”»
onMounted(async () => {
  // ç¡®ä¿DOMå®Œå…¨æ¸²æŸ“
  await nextTick();
  
  // ç«‹å³è§¦å‘åŠ¨ç”»åºåˆ—
  triggerAnimations();
  
  // ä½¿ç”¨Intersection Observerç›‘å¬é¡µé¢æ˜¯å¦å¯è§,ä»¥ä¾¿åœ¨é¡µé¢é‡æ–°è¿›å…¥æ—¶å†æ¬¡è§¦å‘
  animationObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting && entry.target.classList.contains('home-container')) {
        // å½“é¡µé¢é‡æ–°è¿›å…¥è§†çª—æ—¶,å†æ¬¡è§¦å‘åŠ¨ç”»
        setTimeout(() => {
          triggerAnimations();
        }, 100);
      }
    });
  }, {
    threshold: 0.1
  });
  
  animationObserver.observe(document.querySelector('.home-container'));
});

// ç»„ä»¶å¸è½½æ—¶æ–­å¼€è§‚å¯Ÿå™¨
onUnmounted(() => {
  if (animationObserver) {
    animationObserver.disconnect();
  }
});

// é‡æ–°è§¦å‘åŠ¨ç”»çš„å‡½æ•°
const triggerAnimations = () => {
  // ä¸ºæ‰€æœ‰åŠ¨ç”»å…ƒç´ æ·»åŠ åŠ¨ç”»ç±»
  setTimeout(() => {
    // é¦–å…ˆè§¦å‘PixelForge logoçš„å¼¹è·³åŠ¨ç”»
